-- MIGRATION 00044: TICKETS REPLICA IDENTITY → DEFAULT
-- ═══════════════════════════════════════════════════════════════
-- Problem: the `tickets` table was set to REPLICA IDENTITY FULL, which
-- forces PostgreSQL to write the FULL old + new row to the WAL on every
-- UPDATE — not just the primary key + changed columns. With a ~1.4 KB
-- row width and frequent updates from the cron jobs (testing-strike-check,
-- csat-dispatcher, auto-assign) plus normal user activity, this was the
-- single largest contributor to the project's Disk IO budget exhaustion
-- (per the May 2026 alert from Supabase).
--
-- Why FULL is unnecessary here:
--   FULL is only required when realtime subscribers consume `payload.old`
--   (the previous column values) on UPDATE/DELETE events.
--
--   Audit of the codebase (2026-05-21):
--   - use-ticket-realtime.ts passes payload.old to the onUpdate callback
--   - Both consumers (ticket-list-client.tsx, workspace-client.tsx) only
--     read `payload.new` (`raw`). The previous values, when needed, come
--     from React local state (`prev[idx]`), not from the realtime event.
--   - notifications-panel and ticket-detail-client read payload.old only
--     for DELETE events, where it always contains just the primary key
--     regardless of replica identity (RLS limitation per Supabase docs).
--
-- Expected impact: 60–80% reduction in the WAL volume generated by
-- ticket updates, which is the dominant source of realtime IO.
--
-- Reference:
--   https://supabase.com/docs/guides/realtime/postgres-changes
--     ("Receiving `old` record" section)

ALTER TABLE tickets REPLICA IDENTITY DEFAULT;

